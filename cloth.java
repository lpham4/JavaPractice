public class cloth {
   String name, color;
   boolean temp;
   public cloth(String name, String color, boolean temp){
   super();
   this.name = name;
   this.color = color;
   this.temp = temp;
   }
   
   public String getName(){
      return name;
   }
   public String getColor(){
      return color;
   }
   public boolean getTemp(){
      return temp;
   }
   
}